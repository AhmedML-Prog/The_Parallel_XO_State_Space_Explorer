    private void putXO(javax.swing.JButton btn) {
    if (!btn.getText().equals(""))
        return;

    if (xTurn)
        btn.setText("X");
    else
        btn.setText("O");

    xTurn = !xTurn;
    }
    
    private char getValue(javax.swing.JButton btn) {

    if (btn.getText().equals("X"))
        return 'X';

    if (btn.getText().equals("O"))
        return 'O';

    return '-';
    }
