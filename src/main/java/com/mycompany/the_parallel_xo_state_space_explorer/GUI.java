    private void StartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StartActionPerformed
    char[][] board = new char[3][3];

    board[0][0] = getValue(b1);
    board[0][1] = getValue(b2);
    board[0][2] = getValue(b3);

    board[1][0] = getValue(b4);
    board[1][1] = getValue(b5);
    board[1][2] = getValue(b6);

    board[2][0] = getValue(b7);
    board[2][1] = getValue(b8);
    board[2][2] = getValue(b9);

    String mode = Mode.getSelectedItem().toString();
    Explorer ex = new Explorer();

    Panel.setText("");

    try {
        
        if (mode.equals("Single Thread")) {

            long start = System.nanoTime();

            Result r = ex.single(board, 'X');

            long end = System.nanoTime();

            double time = (end - start) / 1_000_000.0;

            Panel.append("Single Thread\n");
            Panel.append("Total Endings = " + r.total + "\n");
            Panel.append("X Wins = " + r.xWins + "\n");
            Panel.append("O Wins = " + r.oWins + "\n");
            Panel.append("Draws = " + r.draws + "\n");
            Panel.append("Time = " + time + " ms");
        }
        
        else if (mode.equals("Parallel")) {

            long start = System.nanoTime();

            Result r = ex.multiThreaded(board, 'X');

            long end = System.nanoTime();

            double time = (end - start) / 1_000_000.0;

            Panel.append("Parallel\n");
            Panel.append("Total Endings = " + r.total + "\n");
            Panel.append("X Wins = " + r.xWins + "\n");
            Panel.append("O Wins = " + r.oWins + "\n");
            Panel.append("Draws = " + r.draws + "\n");
            Panel.append("Time = " + time + " ms");
        }
        
        else if (mode.equals("Virtual Threads")) {

            long start = System.nanoTime();

            Result r = ex.virtual(board, 'X');

            long end = System.nanoTime();

            double time = (end - start) / 1_000_000.0;

            Panel.append("Virtual Threads\n");
            Panel.append("Total Endings = " + r.total + "\n");
            Panel.append("X Wins = " + r.xWins + "\n");
            Panel.append("O Wins = " + r.oWins + "\n");
            Panel.append("Draws = " + r.draws + "\n");
            Panel.append("Time = " + time + " ms");
        }
        else if (mode.equals("All")) {

            Compare c = new Compare();
            c.runAll(board,Panel);
        }

    } catch (Exception e) {
        Panel.setText("Error");}
    }//GEN-LAST:event_StartActionPerformed

